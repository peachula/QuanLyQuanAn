    private void btnNhapActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNhapActionPerformed
        // TODO add your handling code here:
        try
        {
            int num = Integer.parseInt(txtNumber.getText());
            
            if (!ckAm.isSelected())
            {
                if (num < 0)
                    JOptionPane.showMessageDialog(this, "Không được nhập số âm");
                else
                {
                    addList(num);
                }
            }
            else 
            {
                addList(num);
            }
        }
        catch (NumberFormatException e)
        {
             JOptionPane.showMessageDialog(this, "Đừng có nhập chữ má ôi");
        }
        txtNumber.setText("");
    }//GEN-LAST:event_btnNhapActionPerformed

    private void btnChanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChanActionPerformed
        // TODO add your handling code here:
        index_s = new int[n];
        int index_i = 0;
        
        for (int i = 0; i < n; i++) 
        {
            String valuex = String.valueOf(lsNumber.getModel().getElementAt(i));
            int this_num = Integer.parseInt(valuex);
            if (Even(this_num))
            {
                index_s[index_i] = i ;
                index_i = index_i + 1 ;
            }
        }
        
        lsNumber.setSelectionMode(ListSelectionModel.MULTIPLE_INTERVAL_SELECTION);
        lsNumber.setSelectedIndices(index_s);
        lsNumber.setSelectionBackground(Color.gray);
    }//GEN-LAST:event_btnChanActionPerformed

