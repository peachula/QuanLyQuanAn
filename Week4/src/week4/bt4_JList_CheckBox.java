    ///kiểm tra số nguyên tố
    public boolean SNT(int a)
    {
        if (a == 1) return true;
        for (int i = 2; i < a; i++) {
            if (a % i == 0) return false;
        }
        return true;
    }
    
    ///kiểm tra số chẳn
    public boolean Even(int a)
    {
        if (a%2 == 0)
            return true;
        return false;
    }
    
    ///kiểm tra số lẻ
    public boolean Odd(int a)
    {
        if (a%2 != 0)
            return true;
        return false;
    }
    
    ///thêm vào list
    public void addList(int num)
    {
        lsNumber.setModel(listModel);
        listModel.addElement(num);
        
        n = lsNumber.getModel().getSize();
    }
