    public boolean checkNull()
    {
        if (txtName.getText().trim().isEmpty() || txtAddress.getText().trim().isEmpty())
        {
            return true;
        }
        if (SexG.getSelection() == null)
        {
            return true;
        }
        if (!ckReading.isSelected() && !ckSinging.isSelected() && !ckDancing.isSelected())
        {
            return true;
        }
        return false;
    }
    
    private void btnValidateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnValidateActionPerformed
        // TODO add your handling code here:
        if (checkNull()) 
            JOptionPane.showMessageDialog(this, "Fields are empty !!!");
        else
            JOptionPane.showMessageDialog(this, "OK");
    }//GEN-LAST:event_btnValidateActionPerformed

